function verificaDiaDaSemana(n_dia) {
  
  switch (n_dia) {
    case 1:
      return 'É domingo'
    case 2:
      return 'É segunda'
    case 3:
      return 'É terça'
    case 4:
      return 'É quarta'
    case 5:
        return "É quinta"
    case 6:
        return 'É sexta'
    case 7:
        return 'É sábado'
  }

}

